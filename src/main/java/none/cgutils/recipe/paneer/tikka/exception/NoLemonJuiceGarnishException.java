package none.cgutils.recipe.paneer.tikka.exception;

public class NoLemonJuiceGarnishException extends RuntimeException {
    public NoLemonJuiceGarnishException() {
    }

    public NoLemonJuiceGarnishException(String message) {
        super(message);
    }

    public NoLemonJuiceGarnishException(String message, Throwable cause) {
        super(message, cause);
    }

    public NoLemonJuiceGarnishException(Throwable cause) {
        super(cause);
    }

    public NoLemonJuiceGarnishException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
